#include <string.h>

void	ft_init_string(char *dst, size_t size)
{
	size_t	i;
	char	c;

	i = 0;
	c = 33;
	while (i < size)
	{
		dst[i] = c;
		c++;
		if (c == 127)
			c = 33;
		i++;
	}
}
