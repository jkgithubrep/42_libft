int		ft_atoi(const char *str)
{
	int			neg;
	long long	res;

	neg = 1;
	res = 0;
	while (*str && (*str == '\v' || *str == '\r' || *str == '\f' || *str == ' '
				|| *str == '\t' || *str == '\n'))
		str++;
	if (*str == '-' || *str == '+')
		neg = (*str++ == '-') ? -1 : 1;
	while (*str >= '0' && *str <= '9')
		res = res * 10 + (*str++ - '0');
	return (neg * res);
}
